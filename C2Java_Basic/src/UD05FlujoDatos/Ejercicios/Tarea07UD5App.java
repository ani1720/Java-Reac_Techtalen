package UD05FlujoDatos.Ejercicios;

public class Tarea07UD5App {

	public static void main(String[] args) {
		//Usamos un bucle while para imprimir del 1 al 100
		int num = 95;
		// Le decimos al bucle que imprima los numero menores o iguales a 100
		while (num <= 100) {
			// LE decimos que imprima num y vaya sumando 1 en 1 hasta 100
			System.out.println(num);
			num++;
		}

	}

}
