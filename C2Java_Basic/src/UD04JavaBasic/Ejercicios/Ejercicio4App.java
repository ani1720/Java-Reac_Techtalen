package UD04JavaBasic.Ejercicios;

public class Ejercicio4App {

	public static void main(String[] args) {
		//Variable
		int N = 25 ;

		System.out.println("Valor de N "+ N);
			//Incremento de N
		int suma = N + 77;
		System.out.println("N + 77 = "+ suma);
		
		//Decremento de N
		int resta = N - 3;
		System.out.println("N - 3 = "+ resta);
		
		//Duplicar valor
		int dobleN = N * 2 ;
		System.out.println("N * 2 = "+ dobleN);
	}

}
